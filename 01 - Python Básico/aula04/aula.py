# Aula 04 - Tipos de dados "primitivos"
'''
  str - string 'Textos'
  int - inteiro 'números inteiros {...,-3,-2,-1,0,1,2,3,4,5,...}'
  float - real/ponto flutuante '{10.5, 1.5, -10.1, -5.93, 0.98}'
  bool - booleano/lógico 'True/False'
'''

print('Nathalia', type('Nathalia'))
print(10, type(10))
print(25.34, type(25.34))
print(10 == 10, type(10 == 10))

